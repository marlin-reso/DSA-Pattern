package array;

public class MoveZeroToRight {

	public static void main(String[] args) {
		
		int arr[] = {1,0,3,0,0,4,0,5};
		int j=0;
		
		for(int i = 0; i<arr.length;i++) {
			
			if(arr[i] != 0){
				int temp = arr[i];
				arr[i] = arr[j];
				arr[j] = temp;
				j++;
				
			}
		}
		
		for(int a : arr) {
			System.out.println(a);
		}
		

	}

}
