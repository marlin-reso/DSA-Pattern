package array;

public class BasicsOfArray {

	public static void main(String[] args) {
		
		
		int value[]= {1,2,3,4,5,6,7};
		
		char words[]= {'a','b','c','d','e','f'};
		
		for(int numbers : value) {
			
			System.out.println(numbers);
		}
		
		for(char letter: words) {
			
			System.out.println(letter);
		}

	}

}
